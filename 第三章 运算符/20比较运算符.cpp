#include<iostream>
using namespace std;

int main()
{
	//比较运算符
	//相等于 == 
    int a=4;
	int b=3;
	cout<<(a==b)<<endl; 
	
	//不等于 != 
	a=4;
    b=3;
    cout<<(a!=b)<<endl; 
    
    //小于 < 
	a=4;
	b=3;
    cout<<(a<b)<<endl; 
    
    //大于 > 
	a=4;
	b=3;
	cout<<(a>b)<<endl; 
	
	//小于等于 <=
	a=4;
	b=3;
	cout<<(a<=b)<<endl; 
	    
	//大于等于 >= 
	a=4;
	b=3;
	cout<<(a>=b)<<endl; 
	    
	return 0;
}
